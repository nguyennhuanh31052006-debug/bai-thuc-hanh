# Viết chương trình sử dụng các phương thức split và join để tách nhập xâu ký tự

a = 'nguyen nhu anh 18 tuoi'
b = a.split() #lệnh tách chuỗi thành từng từ riêng lẽ
print(b)
c = " ".join(b) #ghép chuỗi lại thành câu bằng dấu cách
print("Câu trên là: " ,c)
